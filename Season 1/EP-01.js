/* How JavaScript Works & Execution Context */

/* 

- Everything in Javascript happens inside the Execution Context
- The Execution Context has two components in it 
   1. Memory component: all variables and functions are stored in key-value pairs, this component is also known as variable environment
   2. Code component: In this component code is executed one line at a time, this component is also known as Thread of Execution
- Javascript is a synchronus single-threaded language 

*/
